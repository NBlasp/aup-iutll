      <h4 class="titulo">PLAN DE 100 DÍAS POR LA AGRICULTURA URBANA</h4>      
      <br>
      <p class="parrafo">La ministra de Agricultura Urbana Lorena Fréitez, señaló que las líneas generales de la iniciativa contemplan el trabajo de registro, articulación, promoción, lanzamiento y distribución de algunos rubros para la población, con el fin de incorporarse al plan de emergencia económica promovido por el Presidente Nicolás Maduro</p>
      <p class="parrafo">En el mes de febrero de 2016, la ministra dio a conocer el plan que desarrollará su despacho en los primeros 100 días de gestión para garantizar la soberanía alimentaria, a través de la implementación de multicultivos en zonas urbanas.</p>
      <p class="parrafo">Luego de conformado registro nacional de agricultura urbana, donde estarán quienes ya producen y aquellas personas que se quieren incorporar, bien sea de manera individual o colectiva; se procede a la capacitación en zonas urbanas y periurbanas, las campañas sobre la creación de huertos, modelos de comunicación, etc.</p>
      <p class="parrafo">Al obtener la cosecha, empieza un plan de distribución, para atender a la familia, luego a la comunidad y finalmente, con costos convenidos de producción se establecerán mecanismos de colocación en los mercados comunales y nacionales.</p>
      <p class="parrafo">La estrategia es determinar cuáles son las zonas de alta densidad, priorizadas, para la colocación de conucos, huertos, o casas de cultivo. Las primeras ciudades donde se desarrollara éste plan serán: Caracas, Los Teques, Barquisimeto, Maracaibo, Barcelona, Mérida, Valencia y Maracay.</p>
      <br>
      <h4 class="titulo">¿Qué se va a producir?</h4>      
      <br>
      <p class="parrafo">En la parte de vegetal, se inicia con lo que se llaman cultivos de ciclo corto, que den resultados rápidos en las condiciones ambientales de nuestro país, estos son:</p>
      <p class="parrafo">Acelga, cilantro, cebollín, berenjena, calabacín, pepino, tomate, cebolla, ají dulce, remolacha, pimentón, zanahoria, lechuga.</p>
      <p class="parrafo">Basada en experiencias exitosas y en los primeros datos levantados por el Ciara (Fundación de Capacitación e Innovación para apoyar la revolución agraria) y el Fondas (Fondo para el Desarrollo Agrario Socialista), éste plan se perfila a alcanzar la construcción de una franja, en las zonas urbanas y periurbanas que bordean nuestras ciudades, para utilizarla como reservorio de alimentos y además generar cientos de empleos.</p>
      <p class="parrafo">Por otra parte, Fréites destacó que iniciarán escuelas de agricultura en todo el país. La Efofac se suma con fuerza, luego los espacios populares, bases de misiones, espacios públicos, parques escuelas.</p>
      <p class="parrafo">En 29 viveros establecidos en distintitos parques del país, se van a establecer espacios de producción de plantas alimentarias, en una primera etapa, que va a exterse a 69 parques y contribuirá a la formación de los jovenes.</p>
      <p class="parrafo">También se crearán Ferias gastronómicas, donde se haga un trabajo pedagógico sobre el calendario agrícola venezolano, ya que al conocerlo las personas pueden aprovechar el potencial de los alimentos por temporadas y así procesarlos para su consumo posterior, siendo una alternativa de ahorro.</p>
      <p class="parrafo">Finalmente, enfatizó que en las próximas semanas desarrollaran una campaña denominada “estilos de vida urbanos”. Esta iniciativa es una nueva forma de que los habitantes de las ciudades no contaminen el ambiente y por el contrario desarrollen un estilo de vida sano, productivo, una alternativa ante el consumo depredador.</p>
