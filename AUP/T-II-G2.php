      <p class="parrafo">Desde el principio empecé  por la rama de los aliños, plantas pequeñas que ocupan poca mantención y atención como lo es el cilantro, tomate e ajíes. Porque son productos muy usados en la cocina  y para mi es mas fácil y de menos esfuerzo y costos que ir los a comprar que yo produciéndolo en mi propia casa, además me da una fuente de entretenimiento y de satisfacción ver mis cultivos crecer.</p>
      <p class="parrafo">Un tiempo después fui explorando otras plantas como el maíz, auyama, y batata, los cuales los desarrolle por sus temporadas, como la batata la cual la coseche en verano la planta tomo una gran parte de mi terreno y en cada parte de su extensión salían muchas de ellas.Lo que si me di de cuenta fue que la tierra es muy dura su tamaño no fueron muy grandes porque la comprimía y no dejara que creciera.Despues de eso sume muchas mas plantas a mi huerto y ahora cuento con una variedad de plantas.</p>
      <p class="parrafo">También en motivo de influenciar a mis vecinos incentivándolos con el resultado de mis cultivos con los resultados magníficos y con muy poco esfuerzo, con una variedad de plantas que se puedes cultivar con muy pocos materiales y que son muy útiles en la cocina. Además te da un aire de independencia  en mis alimentos, porque no me gusta comprar unos ajéis feos y arrugados que venden en las ferias de las verdura me gustan mas mi propios ajéis grandes y mis maíz grandes sin químicos, no como los que produce monzanto.</p>
      <p class="parrafo">Además me gusta compartir mis frutos con mis vecinos e intercambiarlos por otros, como una ves que le cambie una mata de albahaca por una de tomates cherry a mi vecina  Rossy los cuales son muy buenos parea hacer salsa para las pastas con albahaca y orégano, son muy buenos también para hacer ensaladas porque no son necesarios cortarlos y le dan una vista a la ensalada muy bonita. </p>
      <p class="parrafo">Espero tener una gran variedad de plantas en mi huerto para no tener que ir a comprarlas y si un vecino necesita una planta sentirme contenta de tenerla en mi plantación y poder compartirla, es como tener mi propia feria de las verduras privada en donde pueda tener todo sin tener que gastar nada.</p>
      <p class="parrafo">Me alegra participar con el grupo que conforma la actividad de hurto urbano, me permite compartir mis conocimientos que e adquirido y mostrarle la facilidad de cultivar estos productos y así no dejar que se pierda este hobby que tengo tan beneficioso para nosotros, desde luego si todas las personas cultivara algunas plantas en sus casa les ara un bien en la mente y en el bolsillo.</p>
