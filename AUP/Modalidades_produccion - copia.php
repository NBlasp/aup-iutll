      <h4 class="titulo">MODALIDADES DE PRODUCCIÓN DE LA AUP</h4>
      <p class="parrafo">En la Agricultura Urbana y Periurbana se reconocen como modalidades de producción a:</p><br> 

      <h4 class="titulo">Patios Productivos o Conucos</h4>
      <center><img src="img/mod1.jpg" class="img-responsive"></center><br> 
      <p class="parrafo">Los Patios Productivos o Conucos son las unidades de producción desarrolladas en los jardines, patios, o áreas cercanas a las inmediaciones de las casas y viviendas, en las que se pueda cultivar hortalizas, plantas medicinales y ornamentales, producción animal (cría de cachamas, aves, conejos, entre otros animales) para obtener alimentos frescos y sanos durante todo el año.</p><br> 

      <h4 class="titulo">Cultivos Organopónicos</h4>
      <center><img src="img/mod2.jpg" class="img-responsive"></center><br> 
      <p class="parrafo">Los cultivos organopónicos son aquellos espacios improductivos donde se construyen canteros limitados con muros de hasta 30 cm de altura conformados por bloques, maderas, piedras y otros, que luego son llenados con sustratos preparados con mezcla de materia orgánica, cascarilla de arroz o café, y capa vegetal de suelo, para la producción de hortalizas, plantas medicinales y ornamentales.</p><br> 

      <h4 class="titulo">Mesas Organopónicas</h4>
      <center><img src="img/mod3.jpg" class="img-responsive"></center><br> 
      <p class="parrafo">Las mesas organopónicas son construidas con madera u otro material donde se pueden desarrollar cultivos en zonas improductivas de las áreas urbanas: pasillos, patios de cemento, balcones y platabandas.</p><br> 
      
      <h4 class="titulo">Huertos Escolares</h4>
      <center><img src="img/mod4.jpg" class="img-responsive"></center><br> 
      <p class="parrafo">Los Huertos Escolares son terrenos pequeños donde se cultivan hortalizas para consumo de la comunidad escolar, y usualmente funcionan en espacios disponibles dentro de la escuela. Si no hay suficiente terreno, se pueden utilizar materos, botellas plásticas, cajas, mesas, guacales y otros.</p><br> 

      <h4 class="titulo">Huertos Comunitarios</h4>
      <center><img src="img/mod5.jpg" class="img-responsive"></center><br> 
      <p class="parrafo">Los Huertos Comunitarios son espacios recuperados por una comunidad y/o movimiento social dentro del territorio geográfico, en los cuales el cultivo se establece sobre el suelo directamente, ordenados en canteros, y donde se aplica materia orgánica para mejorar su fertilidad. Esta modalidad permite mejorar el paisajismo, incorporar niñas, niños, jóvenes, adultos y ancianos en las actividades agrícolas, aportando a la comunidad la producción intensiva de hortalizas, vegetales y frutas, a bajo costo, sanos y de muy buena calidad.</p><br> 

      <h4 class="titulo">Permacultura</h4>
      <center><img src="img/mod6.jpg" class="img-responsive"></center><br> 
      <p class="parrafo">La Permacultura se considera una ciencia de diseño ético que involucra al ser humano y sus necesidades con el medio ambiente. Es artístico, creativo y paisajístico. Funciona tanto para el pequeño e íntimo espacio del hábitat humano, como para los amplios y dañados ecosistemas. Promueve el reciclaje a través de la siembra en guacales, envases, cauchos, tobos, tubos y otros.</p><br> 
