      <h4 class="titulo">LEGISLACIÓN</h4>
      <br>
      <p class="parrafo">De acuerdo a la Gaceta Oficial N° 40.872 de fecha 18 de marzo de 2016, fue publicado el Decreto N° 2.281 de la Presidencia de la República, mediante el cual se autoriza el cambio de denominación de la Empresa Nacional de Proyectos Agrarios, S.A., adscrita al Ministerio del Poder Popular para la Agricultura Productiva y Tierras, por Corporación Venezolana para la Agricultura Urbana y Periurbana, S.A., y se adscribe al Ministerio del Poder Popular de Agricultura Urbana. Así quedó establecido en los siguientes artículos:</p>

      <p class="parrafo">Artículo 1º. Se autoriza el cambio de denominación de la EMPRESA NACIONAL DE PROYECTOS AGRARIOS, S.A., creada mediante el Decreto Nº 8.819 de fecha 28 de febrero de 2012, publicado en Gaceta Oficial de la República Bolivariana de Venezuela Nº 39.872 de la misma fecha, adscrita al Ministerio del Poder Popular para la Agricultura Productiva y Tierras, por CORPORACIÓN VENEZOLANA PARA LA AGRICULTURA URBANA Y PERIURBANA, S.A., y se adscribe al Ministerios del Poder Popular de Agricultura Urbana, la cual asignará los recursos presupuestarios para la ejecución de los planes y programas.</p>

      <p class="parrafo">Artículo 2º. La CORPORACIÓN VENEZOLANA PARA LA AGRICULTURA URBANA Y PERIURBANA, S.A., tendrá por objeto impulsar el aparato productivo agrícola, pecuario y acuícola, en zonas urbanas y periurbanas, en toda su cadena de valor y servicios conexos, mediante operaciones financieras, a nivel nacional e internacional, que contribuyan a la seguridad y soberanía agroalimentaria y aseguren el acceso, la calidad y el justo valor de los bienes y servicios generados, con la participación protagónica del poder Popular.</p>

      <p class="parrafo">Artículo 3º. Corresponderá a la Ministra o al Ministro del Popular con competencia en materia de Agricultura Urbana ejercer la rectoría de gestión y funcional de la CORPORACIÓN VENEZOLANA PARA LA AGRICULTURA URBANA Y PERIURBANA, S.A. y representará las acciones de la República Bolivariana de Venezuela.</p>

      <p class="parrafo">Artículo 4º. Se ordena realizar el traspaso correspondiente a título gratuito en el Libro de Accionistas de la EMPRESA NACIONAL DE PROYECTOS AGRARIOS, S.A., sobre los bienes y acciones del capital social; a fin de que la Ministra o al Ministro del Popular con competencia en materia de Agricultura Urbana ejerza la tenencia y representación de los bienes y el control accionario correspondiente.</p>

      <p class="parrafo">Artículo 5º. Los Ministros del Poder Popular para la Agricultura Productiva y Tierras y Agricultura Urbana, a partir de la entrada en vigencia de este Decreto, realizarán lo conducente a fin de trasladar al personal o finiquitar la situación administrativa del mismo, así como transferir los bienes y derechos de la EMPRESA NACIONAL DE PROYECTOS AGRARIOS, S.A., a la CORPORACIÓN VENEZOLANA PARA LA AGRICULTURA URBANA Y PERIURBANA, S.A., y harán las gestiones para adecuar los documentos estatutarios a la nueva denominación y objeto de la EMPRESA NACIONAL DE PROYECTOS AGRARIOS, S.A., ordenando efectuar las anotaciones correspondientes, así como su protocolización en el respectivo Registro Mercantil.</p>

      <p class="parrafo">Artículo 6º. Las Ministras o Ministros del Poder Popular con las competencias en materia de Agricultura Productiva y Tierras y Agricultura Urbana, quedan encargados de la ejecución de este Decreto.</p>

      <p class="parrafo">Artículo 7º. Lo no previsto en este Decreto, será resuelto por el Vicepresidente Sectorial de Economía a través de la Resolución publicada a los efectos.</p>

      <p class="parrafo">Artículo 8º. El presente Decreto entrará en vigencia a partir de la fecha de su publicación en la Gaceta Oficial de la República Bolivariana de Venezuela.</p>

      <p class="parrafo">Dado en Caracas a los dieciocho días del mes de marzo de dos mil dieciséis. Años 205º de la Independencia, 157º de la Federación y 17º de la Revolución Bolivariana.</p><br>
      
