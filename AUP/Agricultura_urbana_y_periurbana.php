      <p class="parrafo">La Agricultura Urbana es un sistema económico y ecológico del ámbito urbano – local (zonas urbanas y periurbanas) donde se integran actividades de producción de alimentos, agrícolas, pecuarias, acuícolas, forestales y de procesamiento. Esta producción es complementaria de un sistema agrícola más amplio, y así como en principio permite el autoconsumo familiar-comunal también aporta a un circuito de distribución, comercialización y otras formas de intercambio para satisfacer necesidades alimentarias, medicinales y ambientales saludables de la población.</p><br>

      <h4 class="titulo">Características</h4>
      <ul>
